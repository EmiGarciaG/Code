#include <iostream>
#include <cstdio>
using namespace std;

int main(){
  //Incremento y decremento del valor de una variable
  int a=10;
  cout<<"a="<<a<<endl;
  a++;
  cout<<"a="<<a<<endl;
  a--;
  cout<<"a="<<a<<endl;
  a+=1;
  cout<<"a="<<a<<endl;
  a-=1;
  cout<<"a="<<a<<endl;
  a*=2;
  cout<<"a="<<a<<endl;
  a/=2;
  cout<<"a="<<a<<endl;
}

